export function formatSAR(amount: number | null | undefined, locale: Locale = "en") {
  if (amount == null) return "—";
  return new Intl.NumberFormat(locale === "ar" ? "ar-SA" : "en-SA", {
    style: "currency",
    currency: "SAR",
    maximumFractionDigits: 0,
  }).format(amount);
}
